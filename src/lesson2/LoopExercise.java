package lesson2;

public class LoopExercise {
    public static void main(String[] args) {

        for (int a = 5; a <=10; a++){
            System.out.println(a);
        }
        int b = 15;
        int c = 20;
        while (b <= c){
            System.out.println(b);
            b++;
        }

    }
}
